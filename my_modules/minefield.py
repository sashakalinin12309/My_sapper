def create_minefield(size: int) -> list:

    """
    –§—É–Ω–∫—Ü–∏—è —Å–æ–∑–¥–∞–µ—Ç –º–∞—Ç—Ä–∏—Ü—É size x size (9x9, 15x15 –∏–ª–∏ 21—Ö21), –∫–æ—Ç–æ—Ä–∞—è –±—É–¥–µ—Ç –æ—Å–Ω–æ–≤–æ–π –º–∏–Ω–Ω–æ–≥–æ
    –ø–æ–ª—è.

    –ü–∞—Ä–∞–º–µ—Ç—Ä—ã —Ñ—É–Ω–∫—Ü–∏–∏:
        size: int - —à–∏—Ä–∏–Ω–∞ –∏ –¥–ª–∏–Ω–∞ –º–∞—Ç—Ä–∏—Ü—ã.
    
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç:
        minefield: list - –º–∞—Ç—Ä–∏—Ü–∞ —Å —Ü–∏—Ñ—Ä–∞–º–∏ –∏ –±–æ–º–±–∞–º–∏, —Å–∞–º–æ –ø–æ–ª–µ.
    """
    
    import random  # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –º–æ–¥—É–ª—å random

    counter_bombs: int = 0  # –ü–µ—Ä–µ–º–µ–Ω–Ω–∞—è, –æ—Ç–≤–µ—á–∞—é—â–∞—è –∑–∞ –∫–æ–ª-–≤–æ –±–æ–º–±–æ—á–µ–∫ –≤ –ø–æ–ª–µ.

    # –í—ã—á–∏—Å–ª—è–µ–º —Ç–µ –º–µ—Å—Ç–∞, –≥–¥–µ –Ω–µ –±—É–¥–µ—Ç –±–æ–º–± (–ø–æ—Å–µ—Ä–µ–¥–∏–Ω–µ –∏ —Å–æ—Å–µ–¥–Ω–∏–µ).
    # –í—ã—è—Å–Ω—è–µ–º –∫–æ–ª-–≤–æ –±–æ–º–±, –∫–æ—Ç–æ—Ä–æ–µ –±—É–¥–µ—Ç –∑–∞–≤–∏—Å–µ—Ç—å —Ä–∞–∑–º–µ—Ä–∞ –ø–æ–ª—è.
    if size == 9:
        counter_bombs = 10
        not_bombs = ((3, 3), (3, 4), (3, 5),
                     (4, 3), (4, 4), (4, 5),
                     (5, 3), (5, 4), (5, 5))
    elif size == 15:
        counter_bombs = 35
        not_bombs = ((6, 6), (6, 7), (6, 8), 
                     (7, 6), (7, 7), (7, 8),
                     (8, 6), (8, 7), (8, 8),)
    elif size == 21:
        counter_bombs = 99
        not_bombs = ((9, 9), (9, 10), (9, 11), 
                    (10, 9), (10, 10), (10, 11),
                    (11, 9), (11, 10), (11, 11))

    minefield = [[0 for _ in range(size)] for _ in range(size)]  # –°–æ–∑–¥–∞–µ–º –æ—Å–Ω–æ–≤—É –¥–ª—è –ø–æ–ª—è.
    arithmetic_operations = ('-1' , '-0', '+1')  # –≠—Ç–æ—Ç –∫–æ—Ä—Ç–µ–∂ –Ω—É–∂–µ–Ω –±—É–¥–µ—Ç –¥–ª—è –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –∫–æ–ª-–≤–∞ –±–æ–º–± –≤–æ–∫—Ä—É–≥ –∫–ª–µ—Ç–∫–∏.

    # –í —Å–ª—É—á–∞–π–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ —É–∑–Ω–∞–µ–º –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –±–æ–º–±–æ—á–µ–∫.
    check_list = set(())  # –ú–Ω–æ–∂–µ—Å—Ç–≤–æ, –≤ –∫–æ—Ç–æ—Ä–æ–º –±—É–¥—É—Ç —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã –≤—Å–µ –ù–ï–ü–û–í–¢–û–†–Ø–Æ–©–ò–ï–°–Ø –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã.
    while len(check_list) != counter_bombs:  # –ü–æ–∫–∞ —á–∏—Å–ª–æ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç –±–æ–º–± –Ω–µ —Ä–∞–≤–Ω–æ –∫–æ–ª-–≤—É –±–æ–º–±...
        coordinates = tuple(random.randint(0, size-1) for _ in range(2))  # –ö–æ—Ä—Ç–µ–∂ —Å —Ö –∏ —É.
        # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ —Ç–æ, —á—Ç–æ –±–æ–º–±—ã –Ω–µ –±—É–¥—É—Ç —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω—ã –≤ –∑–æ–Ω–µ, –≥–¥–µ –Ω–∞—á–∏–Ω–∞–µ–º –∏–≥—Ä—É.
        if coordinates not in not_bombs:
            check_list.add(coordinates)

    bombs_list = tuple(check_list)  # –î–ª—è —É–¥–æ–±—Å—Ç–≤–∞ –ø—Ä–µ–≤—Ä–∞—â–∞–µ–º –º–Ω–æ–∂–µ—Å—Ç–≤–æ –≤ –∫–æ—Ä—Ç–µ–∂.
    # –†–∞—Å–ø–æ–ª–æ–≥–∞–µ–º –±–æ–º–±–æ—á–∫–∏ –Ω–∞ –º–∞—Ç—Ä–∏—Ü–µ.
    for coordinates in bombs_list:
        x, y = coordinates[0], coordinates[1]
        minefield[x][y] = "üí£"

    # –°–æ–∑–¥–∞–µ–º –≤ –º–∞—Ç—Ä–∏—Ü–µ —á–∏—Å–ª–∞ –¥–ª—è –∏–≥—Ä—ã.
    for x in range(size):
        for y in range(size):
            if minefield[x][y] != 'üí£':
                counter = 0  # –°—á–µ—Ç—á–∏–∫ –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è —á–∏—Å–ª–∞ –≤ —è—á–µ–π–∫–µ.
                for i in range(3):
                    for j in range(3):
                        
                        # –í—ã—è—Å–Ω—è–µ–º –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã —Å–æ—Å–µ–¥–Ω–∏—Ö –∫–ª–µ—Ç–æ–∫.
                        x_check = int(eval(str(x) + arithmetic_operations[i]))
                        y_check = int(eval(str(y) + arithmetic_operations[j]))
                        
                        # –í—ã—è—Å–Ω—è–µ–º, –≤–æ–∑–º–æ–∂–Ω—ã –ª–∏ —Ç–∞–∫–∏–µ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –∏ —Å–∫–æ–ª—å–∫–æ –±–æ–º–±–æ—á–µ–∫ –≤ —ç—Ç–∏—Ö —è—á–µ–π–∫–∞—Ö.
                        if size > x_check >= 0 and size > y_check >= 0 and minefield[x_check][y_check] == 'üí£':
                            counter += 1

                if counter == 0:
                    minefield[x][y] = " "  # –ö–∞–∫ –±—ã –ø—É—Å—Ç–∞—è —è—á–µ–π–∫–∞, –æ–±–æ–∑–Ω–∞—á–∞—é—â–∞—è, —á—Ç–æ –≤–æ–∫—Ä—É–≥ –Ω–µ—Ç –±–æ–º–±.
                else:
                    minefield[x][y] = str(counter)  # –ê —ç—Ç–æ —É–∂–µ –Ω–µ–ø—É—Å—Ç–∞—è —è—á–µ–π–∫–∞. –ó–¥–µ—Å—å –Ω–∞–¥–æ –±—ã—Ç—å –æ—Å—Ç–æ—Ä–æ–∂–Ω—ã–º –∏–≥—Ä–æ–∫—É!
                
    # –í –∏—Ç–æ–≥–µ –≤–æ–∑—Ä–∞—â–∞–µ–º —ç—Ç—É –º–∞—Ç—Ä–∏—Ü—É.
    return minefield

