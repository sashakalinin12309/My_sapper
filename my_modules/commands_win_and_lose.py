    # –û–ø—è—Ç—å –∂–µ, –ø—Ä–∏–±–∞–≤–ª–µ–Ω–Ω–∞—è —Å—É–º–º–∞ –±—É–¥–µ—Ç –∑–∞–≤–∏—Å–µ—Ç—å –æ—Ç —Ä–µ–∂–∏–º–∞. 
